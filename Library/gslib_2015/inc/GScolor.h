#ifndef		_GSCOLOR_H_
#define		_GSCOLOR_H_

#include	"GSmath.h"

/****** マクロ関数宣言 *******************************************************/

/*=============================================================================
*
* Purpose : カラーを初期化．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColorInit( pOut, fRed, fGreen, fBlue, fAlpha )	\
(															\
	(pOut)->r = (fRed),										\
	(pOut)->g = (fGreen),									\
	(pOut)->b = (fBlue), 									\
	(pOut)->a = (fAlpha)									\
)

/*=============================================================================
*
* Purpose : カラーの加算．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColor3Add( pOut, pC1, pC2 )						\
(															\
	(pOut)->r = CLAMP( (pC1)->r + (pC2)->r, 0.0f, 1.0f ),	\
	(pOut)->g = CLAMP( (pC1)->g + (pC2)->g, 0.0f, 1.0f ),	\
	(pOut)->b = CLAMP( (pC1)->b + (pC2)->b, 0.0f, 1.0f ),	\
	(pOut)->a = CLAMP( (pC1)->a + (pC2)->a, 0.0f, 1.0f )	\
)

/*=============================================================================
*
* Purpose : カラーの減算．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColorSubtract( pOut, pC1, pC2 )					\
(															\
	(pOut)->r = CLAMP( (pC1)->r - (pC2)->r, 0.0f, 1.0f ),	\
	(pOut)->g = CLAMP( (pC1)->g - (pC2)->g, 0.0f, 1.0f ),	\
	(pOut)->b = CLAMP( (pC1)->b - (pC2)->b, 0.0f, 1.0f ),	\
	(pOut)->a = CLAMP( (pC1)->a - (pC2)->a, 0.0f, 1.0f )	\
)

/*=============================================================================
*
* Purpose : カラーのブレンドをする．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColorModulate( pOut, pC1, pC2 )	\
(											\
	(pOut)->r = (pC1)->r * (pC2)->r,		\
	(pOut)->g = (pC1)->g * (pC2)->g,		\
	(pOut)->b = (pC1)->b * (pC2)->b,		\
	(pOut)->a = (pC1)->a * (pC2)->a 		\
)

/*=============================================================================
*
* Purpose : カラーの線形補間．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColorLerp( pOut, pC1, pC2, t )			\
(													\
	(pOut)->r = LERP( (t), (pC1)->r, (pC2)->r ),	\
	(pOut)->g = LERP( (t), (pC1)->g, (pC2)->g ),	\
	(pOut)->b = LERP( (t), (pC1)->b, (pC2)->b ),	\
	(pOut)->a = LERP( (t), (pC1)->a, (pC2)->a )		\
)

/*=============================================================================
*
* Purpose : カラーのスケーリング．
*
* Return  : なし．
*
*============================================================================*/
#define	gsColorScale( pOut, pC, n )					\
(													\
	(pOut)->r = CLAMP( (pC)->r * (n), 0.0f, 1.0f ),	\
	(pOut)->g = CLAMP( (pC)->g * (n), 0.0f, 1.0f ),	\
	(pOut)->b = CLAMP( (pC)->b * (n), 0.0f, 1.0f ),	\
	(pOut)->a = CLAMP( (pC)->a * (n), 0.0f, 1.0f )	\
)

#endif

